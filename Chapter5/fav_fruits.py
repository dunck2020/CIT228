favFruits = ['pineapple', 'blueberries', 'kiwi']
for fruit in favFruits:
    print('The fruit is:', fruit)
    if fruit == 'pineapple':
        print(f'I love a fresh {fruit}\n')
    if fruit == 'blueberries':
        print(f'{fruit.title()} are great in smoothies\n')
    if fruit == 'kiwi':
        print(f'The {fruit} has hairy brown skin\n')
